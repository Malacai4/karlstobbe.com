        An absolutely gorgeous Nicolas Lupot, made in Paris, 1806. Lupot was a
        very special violin maker, often referred to as the French Stradivarius.
        He followed Stradivari’s designs beautifully, with grace and precision.
        He was more than just a copyist though – he was rooted in the classical
        traditions of Stradivari, but was still a forward thinking artisan in
        his own right. Everything about this violin is first rate – workmanship,
        varnish, wood (check out the single-piece maple back), and a great, big,
        beautiful sound. It is a great privilege to have and play this on a
        regular basis.
